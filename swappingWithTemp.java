import java.util.*;

public class Main {
    public static void main(String[] args) {
      int num1 = 200;
      int num2 = 800;
      System.out.println("before swapping num 1="+ num1 + " num2 ="+num2);
      int temp = num1;
      num1 = num2;
      num2 = temp;
      System.out.println("After swapping num 1="+ num1 + " num2 ="+num2);
      
  }
}